# Problem 1: Write a function areaOfCircle(r) which returns the area of a circle of radius r.
# Use the math module.

import math

def areaOfCircle(r):
    return math.pi * r * r

# Example usage
radius = 5
print(f"The area of a circle with radius {radius} is {areaOfCircle(radius):.2f}")
